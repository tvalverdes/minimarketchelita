    public void validar(){
    String dni=txtPass.getText();
    String user=txtUser.getText(); 
        if(txtPass.getText().equals("") || txtUser.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Campos vacíos, ingrese datos");
            txtPass.requestFocus();
        }
        else{
            ev =vdao.ValidarVendedor(dni, user);
            if(ev.getUsuario() != null && ev.getDni()!=null){
                Menu p = new Menu();
                p.setVisible(true);
                dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "Ingrese datos válidos");
                txtPass.requestFocus();
                
                
            }
        }
}
